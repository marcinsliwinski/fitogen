package com.egen.fitogen.model;

public class IssuerProfile {

    private String nurseryName;
    private String country;
    private String countryCode;
    private String postalCode;
    private String city;
    private String street;
    private String phytosanitaryNumber;

    public String getNurseryName() {
        return nurseryName;
    }

    public void setNurseryName(String nurseryName) {
        this.nurseryName = nurseryName;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCountryCode() {
        return countryCode;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getPhytosanitaryNumber() {
        return phytosanitaryNumber;
    }

    public void setPhytosanitaryNumber(String phytosanitaryNumber) {
        this.phytosanitaryNumber = phytosanitaryNumber;
    }
}